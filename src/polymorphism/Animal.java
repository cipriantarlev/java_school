package polymorphism;

public class Animal {

	private int age;
	
	public void eats() {
		System.out.println("Animal eats!!");
	}
	public void sleep() {
		System.out.println("Animal sleeps!!!");
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}

